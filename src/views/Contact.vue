  methods: {
    submitSheet() {
      const vm = this;
      const api = 'https://sheet.best/api/sheets/18a9b910-62fc-4623-a34d-f416af399cd6';
      const data = this.customer;
      vm.$bus.$emit('loading', true);
      vm.$http.post(api, data).then((res) => {
        if (res.status === 200) {
          vm.emailSend(data);
        }
      });
    },
    emailSend(data) {
      const vm = this;
      const serviceId = 'skyice';
      const templateId = 'template_sd';
      emailjs.send(serviceId, templateId, data).then(() => {
        vm.$bus.$emit('loading', false);
        vm.sendState = '寄信成功';
        $('#checkmodal').modal('show');
      }).catch((error) => { console.log(error); });
    },
    toTop() {
      window.scroll(0, 0);
    },
  },
  created() {
    const vm = this;
    vm.$bus.$emit('changeNav', 'contact');
  },
